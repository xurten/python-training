# Tip 25 Use for readability keyword arguments

# * on the arguments list means end of positional arguments and start with only keyword arguments
# / end of positional arguments on the lists
# between / and * we can pass positional arguments and keyword arguments

def safe_division(number, divisor, ignore_overflow, ignore_zero_division):
    try:
        return number / divisor
    except OverflowError:
        if ignore_overflow:
            return 0
        else:
            raise
    except ZeroDivisionError:
        if ignore_zero_division:
            return float('inf')
        else:
            raise


